using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace intro_to_c
{
    class calculator
    {
       public int Adding(int val1, int val2)
        {
            return val1 + val2;
        }

        public int Subtract(int val1, int val2)
        {

            return val1 - val2;
        }

        public int Multiply(int val1, int val2)
        {

            return val1 * val2;
        }
        public float Divide(float val1, float val2)
        {

            return val1 / val2;
        }
    }
}
